<script setup>
import { computed } from 'vue'

// Props
const props = defineProps({
  name: {
    type: String,
    required: true
  },
  size: {
    type: [String, Number],
    default: 'md',
    validator: (value) => {
      // Si c'est un nombre, on l'accepte
      if (typeof value === 'number') return true
      // Si c'est une string, on vérifie les valeurs prédéfinies
      return ['sm', 'md', 'lg', 'xl'].includes(value)
    }
  },
  class: {
    type: String,
    default: ''
  }
})

// Liste des icônes disponibles dans /public/images/icons/
const availableIcons = [
  'check-circle', 'check', 'close', 'edit', 'exclamation', 'eye', 
  'plus', 'refresh', 'search', 'settings', 'trash', 'user', 'warning'
]

// Vérifie si l'icône existe dans le dossier public
const iconExists = computed(() => {
  return availableIcons.includes(props.name)
})

// Classes calculées
const computedClass = computed(() => {
  let sizeClass
  
  // Si size est un nombre, on l'utilise directement
  if (typeof props.size === 'number') {
    sizeClass = `h-${props.size} w-${props.size}`
  } else {
    // Sinon on utilise les tailles prédéfinies
    const sizes = {
      sm: 'h-4 w-4',
      md: 'h-5 w-5',
      lg: 'h-6 w-6',
      xl: 'h-8 w-8'
    }
    sizeClass = sizes[props.size] || sizes.md
  }
  
  return [
    sizeClass,
    props.class
  ].filter(Boolean).join(' ')
})
</script>